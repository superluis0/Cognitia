// Get all topics combined (deduplicated)
export function getAllTopics(): string[] {
  const allTopics = [
    ...PRIORITY_TOPICS,
    ...WIKIPEDIA_VITAL_ARTICLES_L3,
    ...ADDITIONAL_TOPICS,
  ];
  
  // Deduplicate
  const seen = new Set<string>();
  const unique: string[] = [];
  
  for (const topic of allTopics) {
    const normalized = topic.toLowerCase();
    if (!seen.has(normalized)) {
      seen.add(normalized);
      unique.push(topic);
    }
  }
  
  return unique;
}

// Convert Wikipedia slug to Grokipedia URL format
export function toGrokipediaSlug(wikiSlug: string): string {
  // Remove URL encoding and convert to proper format
  let slug = decodeURIComponent(wikiSlug);
  
  // Handle special Wikipedia naming conventions
  slug = slug
    .replace(/_/g, '_')  // Keep underscores
    .replace(/\s+/g, '_'); // Convert spaces to underscores
  
  return slug;
}

// Get Grokipedia URL for a topic
export function getGrokipediaUrl(topic: string): string {
  return `https://grokipedia.com/page/${encodeURIComponent(topic)}`;
}
